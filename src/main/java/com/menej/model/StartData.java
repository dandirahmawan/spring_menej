package com.menej.model;

public class StartData {
	public String name;
	public String email;
	public String unreadNotif;
	public String picProfile;
	public String picProfileDetail;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUnreadNotif() {
		return unreadNotif;
	}
	public void setUnreadNotif(String unreadNotif) {
		this.unreadNotif = unreadNotif;
	}
	public String getPicProfile() {
		return picProfile;
	}
	public void setPicProfile(String picProfile) {
		this.picProfile = picProfile;
	}

	public String getPicProfileDetail() {
		return picProfileDetail;
	}

	public void setPicProfileDetail(String picProfileDetail) {
		this.picProfileDetail = picProfileDetail;
	}
}
